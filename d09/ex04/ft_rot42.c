#include <unistd.h>

char	*ft_rot42(char *str)
{
	int a;

	a = 0;
	while (str[a] != '\0' && ((str[a] <= 122 && str[a] >= 97) ||
				(str[a] <= 90 && str[a] >= 65)))
	{
		if (str[a] <= 122 && str[a] >= 97 && str[a] > 106)
			str[a] = 96 + (16 - (122 - str[a]));
		else if (str[a] <= 90 && str[a] >= 65 && str[a] > 74)
			str[a] = 64 + (16 - (122 - str[a]));
		else
			str[a] = str[a] + 16;
		a++;
	}
	return (str);
}
