#include <unistd.h>

int	ft_str_is_numeric(char *str)
{
	int a;
	int b;

	b = 1;
	a = 0;
	if (str[0] == '\0')
		return (1);
	while (str[a] != '\0')
	{
		if (str[a] > 57 || str[a] < 48)
			return (0);
		else
			b = 1;
		a++;
	}
	return (b);
}
