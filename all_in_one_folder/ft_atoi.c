#include <unistd.h>

int		ft_atoi(char *str)
{
	int a;
	int nb;
	int neg;

	a = 0;
	nb = 0;
	neg = 0;
	while (str[a] == '\n' || str[a] == '\t' || str[a] == '\v' ||
	str[a] == '\f' || str[a] == ' ' || str[a] == '\r')
		a++;
	if (str[a] == '-')
	{
		neg = 1;
		a++;
	}
	if (str[a] == '-' || str[a] == '+')
		a++;
	while (str[a] <= '9' && str[a] >= '0')
	{
		nb = nb * 10 + str[a] - 48;
		a++;
	}
	return (neg == 1 ? nb * -1 : nb);
}
