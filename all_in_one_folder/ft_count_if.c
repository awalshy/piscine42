#include <unistd.h>

int	ft_count_if(char **tab, int (*f)(char*))
{
	int a;
	int b;
	int i;

	i = 0;
	a = 0;
	b = 0;
	while (tab[a])
	{
		if (f(tab[a]) == 1)
			i++;
		a++;
	}
	return (i);
}
