#include <stdlib.h>

char	*ft_strdup(char *src)
{
	int		a;
	int		b;
	char	*s;

	a = 0;
	b = 0;
	while (src[b] != '\0')
		b++;
	s = (char*)malloc(sizeof(*src) * (b + 1));
	while (src[a] != '\0')
	{
		s[a] = src[a];
		a++;
	}
	s[a] = '\0';
	return (s);
}
